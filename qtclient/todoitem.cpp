#include "todoitem.h"

TodoItem::TodoItem(const QString& id, const QString& name, bool done, bool processing) :
    m_id(id),
    m_name(name),
    m_done(done),
    m_processing(processing)
{}

QString TodoItem::id() const { return m_id; }
QString TodoItem::name() const { return m_name; }
bool TodoItem::done() const { return m_done; }
bool TodoItem::processing() const { return m_processing; }

void TodoItem::setId(const QString& id) { m_id = id; }
void TodoItem::setName(const QString& name) { m_name = name; }
void TodoItem::setDone(bool done) { m_done = done; }
void TodoItem::setProcessing(bool processing) { m_processing = processing; }
